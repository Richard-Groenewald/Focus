      CHECK ((channel = ANY (ARRAY['Email'::text, 'Call'::text, 'LinkedIn'::text, 'Visit'::text, 'Other'::text])))
);

CREATE INDEX IF NOT EXISTS campaign_cadence_steps_campaign_idx
  ON public.campaign_cadence_steps (research_campaign_id);

GRANT ALL ON TABLE public.campaign_cadence_steps TO anon;
GRANT ALL ON TABLE public.campaign_cadence_steps TO authenticated;
GRANT ALL ON TABLE public.campaign_cadence_steps TO service_role;

-- ───────────────────────────────────────────── sql/add_branches.sql
-- Branches above Regions.
-- Introduces a `branches` lookup that sits ABOVE the existing `regions`:
--   branches (1) → regions (N).
-- Records (deals/leads/orgs/campaigns) gain their own branch_id that auto-fills
-- from the region's branch but is overridable. Users can be scoped at either
-- level (system_user_regions stays; new system_user_branches added).
-- Idempotent — safe to re-run.

-- 1. branches lookup (mirrors public.regions) ------------------------------
CREATE TABLE IF NOT EXISTS public.branches (
    id         bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name       text NOT NULL UNIQUE,
    active     boolean DEFAULT true NOT NULL,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    created_by bigint REFERENCES public.people(id) ON DELETE SET NULL
);
GRANT ALL ON TABLE public.branches TO anon;
GRANT ALL ON TABLE public.branches TO authenticated;
GRANT ALL ON TABLE public.branches TO service_role;

-- 2. regions.branch_id — a region belongs to one branch -------------------
ALTER TABLE public.regions
  ADD COLUMN IF NOT EXISTS branch_id bigint REFERENCES public.branches(id) ON DELETE SET NULL;

-- 3. branch_id on the records that already carry region_id ----------------
ALTER TABLE public.deals
  ADD COLUMN IF NOT EXISTS branch_id bigint REFERENCES public.branches(id) ON DELETE SET NULL;
ALTER TABLE public.leads
  ADD COLUMN IF NOT EXISTS branch_id bigint REFERENCES public.branches(id) ON DELETE SET NULL;
ALTER TABLE public.organisations
  ADD COLUMN IF NOT EXISTS branch_id bigint REFERENCES public.branches(id) ON DELETE SET NULL;
ALTER TABLE public.research_campaigns
  ADD COLUMN IF NOT EXISTS branch_id bigint REFERENCES public.branches(id) ON DELETE SET NULL;
ALTER TABLE public.sales_campaigns
  ADD COLUMN IF NOT EXISTS branch_id bigint REFERENCES public.branches(id) ON DELETE SET NULL;

-- 4. Branch-level user scoping (mirrors system_user_regions) --------------
CREATE TABLE IF NOT EXISTS public.system_user_branches (
    id             bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    system_user_id bigint NOT NULL REFERENCES public.system_users(id) ON DELETE CASCADE,
    branch_id      bigint NOT NULL REFERENCES public.branches(id) ON DELETE CASCADE,
    created_at     timestamp with time zone DEFAULT now() NOT NULL,
    created_by     bigint REFERENCES public.people(id),
    CONSTRAINT system_user_branches_user_branch_key UNIQUE (system_user_id, branch_id)
);
CREATE INDEX IF NOT EXISTS idx_system_user_branches_user
  ON public.system_user_branches USING btree (system_user_id);
GRANT ALL ON TABLE public.system_user_branches TO anon;
GRANT ALL ON TABLE public.system_user_branches TO authenticated;
GRANT ALL ON TABLE public.system_user_branches TO service_role;

