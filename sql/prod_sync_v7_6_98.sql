UPDATE public.deals              d SET branch_id = r.branch_id FROM public.regions r WHERE d.region_id = r.id AND d.branch_id IS NULL;
UPDATE public.leads              l SET branch_id = r.branch_id FROM public.regions r WHERE l.region_id = r.id AND l.branch_id IS NULL;
