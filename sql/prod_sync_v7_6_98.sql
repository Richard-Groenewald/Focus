  updated_at                 TIMESTAMPTZ  NOT NULL DEFAULT now(),
  PRIMARY KEY (post_id, discretionary_incentive_id)
);

-- ── 5. POST × STATUTORY ALLOWANCES (toggles) ─────────────────────────
-- These are pure toggles — the rate lives on quote_statutory_allowances
